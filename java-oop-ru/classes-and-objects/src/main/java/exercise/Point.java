package exercise;

// BEGIN
public final class Point {

    private Integer dotX;
    private Integer dotY;

    public Point(Integer x, Integer y) {
        this.dotX = x;
        this.dotY = y;
    }

    public Point() {

    }
    public Integer getX() {
        return dotX;
    }
    public Integer getY() {
        return dotY;
    }
}
// END
